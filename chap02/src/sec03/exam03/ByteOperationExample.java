package sec03.exam03;

public class ByteOperationExample {
    public static void main(String[] args) {
        byte result = 10 + 20;
        System.out.println(result);         //byte 타입 30

        byte x = 10;
        byte y = 20;
        int result2 = x + y;
        System.out.println(result2);        //int 타입 30
    }
}
